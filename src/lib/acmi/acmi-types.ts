// ---------------------------------------------------------------------------
// §1 — Key structure
// ---------------------------------------------------------------------------

/**
 * Canonical ACMI namespaces used across the fleet.
 * The protocol accepts any `[a-z][a-z0-9_-]*` string, but the fleet
 * conventions enforce these seven for cross-agent coordination.
 */
export type AcmiNamespace =
  | 'agent'     // Autonomous LLM-backed or scripted agents
  | 'user'      // Human team members (actor_type: "human")
  | 'thread'    // Cross-fleet coordination channels (append-only)
  | 'work'      // Cross-session projects, tasks, ideas
  | 'project'   // Long-running deliverables (broader than work)
  | 'session'   // Per-session ephemera (auto-generated by acmi_spawn)
  | 'registry'  // Cross-cutting metadata (fleet directory, policy, brand)
  | 'hitl'      // Human-in-the-loop queues
  | (string & {}); // Extensible — allows custom namespaces

/**
 * The three data slots every ACMI entity has.
 */
export type AcmiSlot = 'profile' | 'signals' | 'timeline';

/**
 * Full Redis key: `acmi:<namespace>:<id>:<slot>`
 */
export type AcmiKey = `acmi:${AcmiNamespace}:${string}:${AcmiSlot}`;

/**
 * Tenant-scoped key prefix (v1.3 multi-tenant).
 * `acmi:tenant:<tenant_id>:<namespace>:<id>:<slot>`
 */
export type AcmiTenantKey = `acmi:tenant:${string}:${AcmiNamespace}:${string}:${AcmiSlot}`;

/** Valid actor types per SPEC §11 — Multi-actor */
export type AcmiActorType = 'agent' | 'human' | 'system' | 'external';

/** Valid HITL statuses */
export type AcmiHitlStatus = 'open' | 'closed';

/** Role an agent can have in a thread */
export type AcmiThreadRole = 'participant' | 'lead';

/** Work item status ladder */
export type AcmiWorkStatus =
  | 'DRAFT'
  | 'RATIFIED'
  | 'IN_PROGRESS'
  | 'SHIPPED'
  | 'CANCELLED';

/** Work item priority */
export type AcmiWorkPriority = 'P0' | 'P1' | 'P2' | 'P3';

/** Speaker type for distinguishing agent reasoning from human input (v1.3) */
export type AcmiSpeakerType = 'agent' | 'human' | 'system';

// ---------------------------------------------------------------------------
// §2 — Profile
// ---------------------------------------------------------------------------

/**
 * ACMI Profile — the "who" slot.
 *
 * Stores identity metadata for any entity. v1.3 requires `actor_type`.
 * The `tenant_id` field enables multi-tenant isolation (SPEC §12).
 * Additional arbitrary fields are allowed via the index signature.
 */
export interface AcmiProfile {
  /** Actor type (v1.3 required): 'agent', 'human', 'system', 'external' */
  actor_type: AcmiActorType;

  /** Tenant isolation key (v1.3 optional). Default: "madez" */
  tenant_id?: string;

  /** Human-readable display name */
  name?: string;

  /** Functional role within the fleet */
  role?: string;

  /** VM identifier or deployment label (MCP agents) */
  host_id?: string;

  /** Free-form portfolio string (skills, capabilities) */
  description?: string;

  /** Arbitrary additional profile fields */
  [key: string]: unknown;
}

/**
 * Profile document for registry:fleet:* entries.
 * Extends AcmiProfile with fleet-specific metadata.
 */
export interface AcmiFleetProfile extends AcmiProfile {
  /** The agent's primary role in the fleet */
  fleet_role?: string;

  /** Threads this agent is typically active in */
  primary_threads?: string[];

  /** Skill tags for capability discovery */
  skills?: string[];

  /** UTC wake window, e.g. "00:00-23:59" */
  wake_window_utc?: string;

  /** The agent's primary entity ID (mirrors the key's id) */
  primary_id?: string;
}

// ---------------------------------------------------------------------------
// §3 — Signals
