/**
 * CorrelationId format: `<descriptiveCamelCase>-<msEpoch>`
 * e.g., "claudeEngineerSmitheryQualityWin-1778174324664"
 */
export type AcmiCorrelationId = `${string}-${number}`;

/**
 * A correlation chain — a sequence of events linked by correlationId
 * and parentCorrelationId, forming a directed acyclic graph.
 */
export interface AcmiCorrelationChain {
  /** The root correlationId */
  rootCorrelationId: string;
  /** All events in this chain, sorted by ts ascending */
  events: AcmiEvent[];
  /** The entity timelines these events were collected from */
  sources: string[];
}

