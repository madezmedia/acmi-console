/**
 * ACMI Signals — the "now" slot.
 *
 * A mutable KV map of JSON values representing current state.
 * Every key is a string (1–128 chars); values are arbitrary JSON.
 */
export interface AcmiSignals {
  [key: string]: unknown;
}

/**
 * Typed signal accessor helper.
 * Use `getSignal<T>(signals, key, fallback?)` to read a typed value.
 */
export type SignalValue<T = unknown> = T | undefined;

// ---------------------------------------------------------------------------
// §4 — Timeline Events
// ---------------------------------------------------------------------------

/**
 * ACMI Timeline Event (Comms v1.5 envelope).
 *
 * The core data structure — a timestamped, correlated event that forms
 * the append-only history of an entity.
 *
 * Required fields (Comms v1.1):
 *   ts, source, kind, correlationId, summary
 *
 * v1.5 additions:
 *   parentCorrelationId, payload (typed), tags, speaker_type
 */
export interface AcmiEvent {
  /** Wall-clock ms. Monotonicity NOT required across writers. */
  ts: number;

  /**
   * Who wrote the event. Entity-ID format: `"agent:<id>"`, `"user:<id>"`.
   * Examples: "agent:claude-engineer", "user:mikey"
   */
  source: string;

  /**
   * Event taxonomy — a verb describing what happened.
   * Fleet convention uses kebab-case: "milestone-shipped", "handoff-ack", etc.
   */
  kind: string;

  /**
   * Chain identifier. Format: `<descriptiveCamelCase>-<msEpoch>`.
   * Enables tracing across entities and sessions.
   */
  correlationId: string;

  /**
   * Human-readable one-line summary, ≤500 chars.
   * Fleet convention: `[<kind-tag> @<recipient> ...] <description>`
   */
  summary: string;

  /**
   * Links this event to a parent correlationId, forming a chain.
   * The parent event may exist on a different entity's timeline.
   */
  parentCorrelationId?: string;

  /**
   * Arbitrary structured payload. SDK round-trips losslessly.
   * Common conventions: `logAssessment` (0-100 RL score), `thought`, `entryId`
   */
  payload?: AcmiEventPayload;

  /** Free-form string tags for filtering and categorization */
  tags?: string[];

  /**
   * v1.3 — distinguishes synthesizable agent reasoning from
   * lived-experience human input. May become REQUIRED in v1.4.
   */
  speaker_type?: AcmiSpeakerType;

  /** Arbitrary additional fields — adapters round-trip losslessly */
  [key: string]: unknown;
}

/**
 * Typed payload for ACMI events.
 * Extends Record<string, unknown> to allow arbitrary keys while providing
 * known field autocompletion.
 */
export interface AcmiEventPayload extends Record<string, unknown> {
  /** RL self-assessment score (0-100). Convention for *-completed / *-shipped events */
  logAssessment?: number;

  /** Free-text rubric documenting the logAssessment */
  rubric?: string;

  /** Entry ID (journal-specific convention) */
  entryId?: string;

  /** Embedded thought text */
  thought?: string;
}

/**
 * Event selection/query options for timeline reads.
 */
export interface AcmiTimelineOptions {
  /** Maximum number of events to return */
  limit?: number;

  /** Reverse chronological order (default: true) */
  reverse?: boolean;

  /** Only events with ts >= sinceMs */
  since?: number;

  /** Only events with ts <= untilMs */
  until?: number;
}

/** Sorting direction for timeline queries */
export type AcmiSortDirection = 'asc' | 'desc';

/** Field to sort timeline events by */
export type AcmiSortField = 'ts' | 'kind' | 'source';

/** Sort specification */
export interface AcmiSortSpec {
  field: AcmiSortField;
  direction: AcmiSortDirection;
}

/** Filter specification for timeline queries */
export interface AcmiEventFilter {
  /** Filter by exact kind match */
  kinds?: string[];
  /** Filter by source prefix (e.g., "agent:claude-engineer") */
  sources?: string[];
  /** Filter by correlationId prefix */
  correlationIdPrefix?: string;
  /** Filter by tag (event must have at least one matching tag) */
  tags?: string[];
  /** Filter by speaker_type */
  speakerTypes?: AcmiSpeakerType[];
}

