/**
 * Standard event kinds used across the fleet.
 * Not exhaustive — agents may define custom kinds, but these are the
 * canonical verbs from the namespace guide.
 */
export const AcmiEventKinds = {
  /** A choice was made (often by a human via HITL) */
  DECISION: 'decision',
  /** One actor handed work to another */
  TASK_DELEGATION: 'task-delegation',
  /** The receiver of a delegation acknowledged ownership */
  HANDOFF_ACK: 'handoff-ack',
  /** A discrete deliverable is done */
  MILESTONE_SHIPPED: 'milestone-shipped',
  /** Work item lifecycle */
  WORK_CREATED: 'work-created',
  WORK_UPDATE: 'work-update',
  WORK_COMPLETED: 'work-completed',
  WORK_RATIFIED: 'work-ratified',
  /** Operational incident chain */
  INCIDENT_OPENED: 'incident-opened',
  INCIDENT_UPDATE: 'incident-update',
  INCIDENT_RESOLVED: 'incident-resolved',
  /** A scoping or prioritization call */
  SCOPE_DECISION: 'scope-decision',
  /** Lightweight cross-agent visibility note */
  COORD_NOTE: 'coord-note',
  /** Liveness signal */
  HEARTBEAT: 'heartbeat',
  /** A file/URL was shipped to a known location */
  ARTIFACT_PUBLISHED: 'artifact-published',
  /** Periodic broadcast to multiple recipients */
  TEAM_LOOP: 'team-loop',
  /** A deprecated entity path was migrated */
  NAMESPACE_ARCHIVED: 'namespace-archived',
  /** Human-in-the-loop queue events */
  HITL_REQUIRED: 'hitl-required',
  HITL_RESOLVED: 'hitl-resolved',
  /** Journal entry (Folana's Journal convention) */
  JOURNAL_ENTRY: 'journal_entry',
  /** Session lifecycle */
  SPAWN: 'spawn',
} as const;

export type AcmiEventKind = (typeof AcmiEventKinds)[keyof typeof AcmiEventKinds];

